operandoA = 3
operandoB = 2
suma = operandoA + operandoB
print("El resultado es:", suma)
# con f puedo insertar variables dentro de una cadena con {}: ejemplo
print(f"El resultado es: {suma} ")
# la forma acatual mas aceptada para imprimir información
resta = operandoA - operandoB
multiplicacion = operandoA * operandoB
division = operandoA/operandoB
print(f"resultado multiplicacion: {multiplicacion} Resultado division: {division} Resultado resta = {resta}")
# función modulo "%" es el residuo de la division, lo q sobra de la parte entera
modulo = operandoA % operandoB
print(f"Resultado residuo de la division: {modulo}")
exponente = operandoA ** operandoB
print(f"Resultado exponente: {exponente}")